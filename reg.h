#define REG_RCC_CR2_HSI14CAL_SHIFT  8
#define REG_RCC_CR2_HSI14CAL_MASK   MASK32(REG_RCC_CR2_HSI14CAL_SHIFT, 8)
#define REG_RCC_CR2_HSI48ON         BIT32(16)
#define REG_RCC_CR2_HSI48RDY        BIT32(17)
#define REG_RCC_CR2_HSI48CAL_SHIFT  24
