// Reset and Clock Control (RCC)
//

#define REG_RCC_CR_OFFSET       0x00 // Clock control register
#define REG_RCC_CFGR_OFFSET     0x04 // Clock configuration register
#define REG_RCC_CIR_OFFSET      0x08 // Clock interrupt register
